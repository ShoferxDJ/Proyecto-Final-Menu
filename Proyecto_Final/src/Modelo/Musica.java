package Modelo;

public class Musica {
private int id, precio,num;
private String genero, nom,desc;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getPrecio() {
	return precio;
}
public void setPrecio(int precio) {
	this.precio = precio;
}
public int getNum() {
	return num;
}
public void setNum(int num) {
	this.num = num;
}
public String getGenero() {
	return genero;
}
public void setGenero(String genero) {
	this.genero = genero;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public String getDesc() {
	return desc;
}
public void setDesc(String desc) {
	this.desc = desc;
}

}

